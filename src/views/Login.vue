<script>
import qs from 'qs'


export default {
  name: "Login",
  data() {
    return {
      user: {
        username: "",
        password: "",
      },
      rules: {
        username: [
          {required: true, message: '请输入用户名', trigger: 'blur'}, //blur表示鼠标失去焦点的时候触发此校验
